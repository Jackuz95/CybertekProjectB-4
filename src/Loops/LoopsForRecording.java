package Loops;

public class LoopsForRecording {
    public static void main(String[] args) {

        for(int i=0; i<5; i++){
            System.out.println("App monsters are the best!" );
        }


        int n=0;
        while (n<5){
            System.out.println("App monsters are the best!");
            n++;
        }


        int a=0;
         do {
             System.out.println("App monsters are the best!");
             a++;
         } while(a<5);

    }

}
