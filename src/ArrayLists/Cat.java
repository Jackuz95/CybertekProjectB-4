package ArrayLists;

public class Cat {
    String color;
    int age;

    public void eat(){
        System.out.println("Cat is eating");
    }
    public void sleep(){
        System.out.println("Cat is sleeping");
    }
    public void play(){
        System.out.println("Cat is playing");
    }
    public void info(){
        System.out.println("color "+ color );
        System.out.println("Age is " + age);
    }
}
